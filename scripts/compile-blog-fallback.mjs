/** No npm deps — compile content/blog for CI/local when gray-matter missing */
import fs from "fs";
import path from "path";
import { spawnSync } from "child_process";

const ROOT = path.resolve(".");
const OUT = path.join(ROOT, "src/data/blog/posts.generated.ts");

function parseFrontmatter(raw) {
  const m = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!m) return { data: {}, body: raw };
  const data = {};
  for (const line of m[1].split("\n")) {
    const kv = line.match(/^(\w+):\s*(.+)$/);
    if (!kv) continue;
    let v = kv[2].trim();
    if (v.startsWith('"') && v.endsWith('"')) v = v.slice(1, -1);
    if (kv[1] === "tags") {
      data.tags = [...v.matchAll(/"([^"]+)"/g)].map((x) => x[1]);
    } else data[kv[1]] = v;
  }
  return { data, body: m[2] };
}

function mdToHtml(md) {
  return md
    .split(/\n\n+/)
    .map((block) => {
      block = block.trim();
      if (!block) return "";
      if (block.startsWith("## ")) return `<h2>${esc(block.slice(3))}</h2>`;
      if (block.startsWith("- [ ]")) {
        return `<ul>${block
          .split("\n")
          .map((l) => `<li>${esc(l.replace(/^- \[ \] /, ""))}</li>`)
          .join("")}</ul>`;
      }
      if (block.startsWith("- ")) {
        return `<ul>${block
          .split("\n")
          .map((l) => `<li>${esc(l.replace(/^- /, ""))}</li>`)
          .join("")}</ul>`;
      }
      if (/^\d+\./.test(block)) {
        return `<ol>${block
          .split("\n")
          .map((l) => `<li>${esc(l.replace(/^\d+\.\s*/, ""))}</li>`)
          .join("")}</ol>`;
      }
      let html = esc(block);
      html = html.replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>");
      html = html.replace(/\*([^*]+)\*/g, "<em>$1</em>");
      return `<p>${html}</p>`;
    })
    .join("\n");
}

function esc(s) {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

const posts = [];
for (const lang of ["id", "en"]) {
  const dir = path.join(ROOT, "content/blog", lang);
  if (!fs.existsSync(dir)) continue;
  for (const file of fs.readdirSync(dir)) {
    if (!file.endsWith(".mdx")) continue;
    const raw = fs.readFileSync(path.join(dir, file), "utf8");
    const { data, body } = parseFrontmatter(raw);
    const slug = file.replace(/\.mdx$/, "");
    const row = {
      slug,
      lang,
      title: data.title ?? slug,
      description: data.description ?? "",
      date: data.date ?? "",
      tags: data.tags ?? [],
      html: mdToHtml(body),
    };
    if (data.ogImage) row.ogImage = data.ogImage;
    posts.push(row);
  }
}
posts.sort((a, b) => (a.date < b.date ? 1 : -1));

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(
  OUT,
  `/* AUTO-GENERATED by scripts/compile-blog-fallback.mjs */
import type { BlogPost } from "@/types/blog";

export const blogPostsGenerated: BlogPost[] = ${JSON.stringify(posts, null, 2)} as BlogPost[];
`,
);
console.log("blog fallback:", posts.length, "posts");
spawnSync(process.execPath, ["scripts/generate-sitemap.mjs"], {
  cwd: ROOT,
  stdio: "inherit",
  env: { ...process.env, VITE_SITE_URL: process.env.VITE_SITE_URL || "https://appvibe.web.id" },
});